public class If_else {
    public static void main(String[] args) {

        //if else
        //when the condition is true it going the if block
        //when the condition is false it going the else block
        if (true){
            System.out.println("hello world");
        }
        int num = 12;
        if (num ==13){
            System.out.println("yes");
        } else {
            System.out.println("No");
        }

        //while loop
        //hint - when you don't know the loop is running the used while loop
        int count = 1;
        while (count != 5){
            System.out.println(count);
            count++;
        }
        System.out.println("");


        //For loop
        //hints - when you know the loop is running the used for loop
        for (int i = 1; i < 10; i++) {
            System.out.println(i);
        }
    }
}
